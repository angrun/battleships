CREATE TABLE orders (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  orderNumber VARCHAR(255) NOT NULL
);

CREATE TABLE ordersRows (
  id       BIGINT,
  itemName VARCHAR(255) NOT NULL,
  quantity INT,
  price    INT
);
